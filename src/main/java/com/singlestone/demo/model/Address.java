package com.singlestone.demo.model;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Pattern;
import javax.validation.constraints.Positive;
import javax.validation.constraints.Size;

import com.fasterxml.jackson.annotation.JsonIgnore;
import com.singlestone.demo.resource.exceptions.ExceptionMessageConstants;

@Entity
public class Address {

	@Positive
	@Id
	@GeneratedValue
	@JsonIgnore
	private int id;

	@NotNull
	@Size(min = 2, message = ExceptionMessageConstants.MIN_STREET_NAME)
	@Size(max = 100, message = ExceptionMessageConstants.MAX_STREET_NAME)
	private String street;

	@NotNull
	@Size(min = 2, message = ExceptionMessageConstants.MIN_CITY_NAME)
	@Size(max = 100, message = ExceptionMessageConstants.MAX_CITY_NAME)
	private String city;

	@NotNull
	@Size(min = 2, max = 2, message = ExceptionMessageConstants.MAX_MIN_STATE_CODE)
	private String state;

	@NotNull
	@Pattern(regexp = "^[0-9]{5}(?:-[0-9]{4})?$", message = ExceptionMessageConstants.INVALID_ZIP_CODE)
	private String zip;

	public Address() {
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getStreet() {
		return street;
	}

	public void setStreet(String street) {
		this.street = street;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getZip() {
		return zip;
	}

	public void setZip(String zip) {
		this.zip = zip;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((city == null) ? 0 : city.hashCode());
		result = prime * result + id;
		result = prime * result + ((state == null) ? 0 : state.hashCode());
		result = prime * result + ((street == null) ? 0 : street.hashCode());
		result = prime * result + ((zip == null) ? 0 : zip.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Address other = (Address) obj;
		if (city == null) {
			if (other.city != null)
				return false;
		} else if (!city.equals(other.city))
			return false;
		if (id != other.id)
			return false;
		if (state == null) {
			if (other.state != null)
				return false;
		} else if (!state.equals(other.state))
			return false;
		if (street == null) {
			if (other.street != null)
				return false;
		} else if (!street.equals(other.street))
			return false;
		if (zip == null) {
			if (other.zip != null)
				return false;
		} else if (!zip.equals(other.zip))
			return false;
		return true;
	}

}
